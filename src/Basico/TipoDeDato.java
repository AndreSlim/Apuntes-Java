package Basico;

public class TipoDeDato {

    public static void main(String[] args) {

        // Datos primitivos
        // Tipos de datos que no representan objetos.
        int numeroPrimitivo = 123;


        // Datos No primitivos
        // Tipos de datos que representan objetos.
        Integer numeroNoPrimitivo = 123;
        // Los datos primitivos cuentan con métodos para realizar acciones


        // Variable constante que no podrá ser cambiada
        final int numeroConstante = 456;

    }

}
